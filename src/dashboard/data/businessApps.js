export const getAppBySlug = (slug) =>
  businessApps.find((app) => app.slug === slug);

export const getAppById = (id) => businessApps.find((app) => app.id === id);

export function enrichAppForSidebar(app) {
  const detailRoute = `/dashboard/apps/${app.slug}`;
  const route = app.route || detailRoute;

  const defaultMenu =
    app.route && app.route !== detailRoute
      ? [
          { label: "Overview", href: route },
          { label: "App page", href: detailRoute },
        ]
      : [{ label: "Overview", href: detailRoute }];

  return {
    ...app,
    sidebarLabel: app.sidebarLabel || app.name,
    route,
    menu: app.menu?.length ? app.menu : defaultMenu,
  };
}

export const getInstalledApps = (installedIds) =>
  businessApps
    .filter((app) => installedIds.includes(app.id))
    .map(enrichAppForSidebar);
